// Escreva um loop JavaScript for que irá iterar de 0 a 15. Para cada iteração, ele verificará se o número atual é par ou ímpar e exibirá uma mensagem na tela. Vá para o editor
// Exemplo de saída:
// "0 é par"
// "1 é ímpar"
// "2 é par"

for (let c = 1; c <= 15; c++) {
    if (c % 2 == 0) {
        console.log(`${c} é par`)
    } else {
        console.log(`${c} é ímpar`)
    }
}