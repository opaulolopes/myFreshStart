# Dobro, Triplo, Raiz Quadrada
num = int(input('Digite um número: '))
print(f'O dobro de {num} vale {2*num}.')
print(f'O triplo de {num} vale {3*num}.')
print(f'A raiz quadrada de {num} vale {num**(1/2):.2f}.')

